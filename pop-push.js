"use strict"


const chores = ['wash dishes', 'do laundry', 'take out trash', 'cook dinner', 'mop floor'];

chores.pop();
console.log(chores);

// result 'wash dishes', 'do laundry', 'take out trash', 'cook dinner'
// pop is removing the last element from the array.



const chores = ['wash dishes', 'do laundry', 'take out trash', 'cook dinner', 'mop floor'];

chores.push('mayo');
console.log(chores);

// result 'wash dishes', 'do laundry', 'take out trash', 'cook dinner', 'mayo'
// push is adding an element to end of the array.
